def force_num(num,force):
    print(num**force)

force_num(2,10)

def multiplication_num(num_1,multiplication):
    print(num_1*multiplication)

multiplication_num(3,10)

def division_num(num_1,division):
    print(num_1/division)

division_num(15,6)

def difference_num(num_1,difference):
    print(num_1-difference)

difference_num(18,7)

def addition_num(num_1,addition):
    print(num_1+addition)

addition_num(3,5)

